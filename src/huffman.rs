use anyhow::{bail, Result};

use crate::bit::BitReader;

/// Canonical Huffman table (JPEG-style) built from code length counts (bits) and symbols (huffval).
///
/// `bits[i]` is the number of codes of length (i+1), for i=0..15.
#[derive(Debug, Clone)]
pub struct HuffmanTable {
    // For each code length L=1..16, store the first code and the number of codes.
    first_code: [u16; 17],
    count: [u16; 17],
    // For each code length, store the index in `symbols` where codes of that length begin.
    first_symbol: [u16; 17],
    symbols: Vec<u8>,
}

impl HuffmanTable {
    pub fn from_jpeg_bits_values(bits: &[u8; 16], values: &[u8]) -> Result<Self> {
        let mut count = [0u16; 17];
        let mut first_code = [0u16; 17];
        let mut first_symbol = [0u16; 17];

        let mut total: usize = 0;
        for (i, &c) in bits.iter().enumerate() {
            count[i + 1] = c as u16;
            total += c as usize;
        }
        if total != values.len() {
            bail!("huffman table: bits total {} != values len {}", total, values.len());
        }

        // Canonical codes.
        let mut code: u16 = 0;
        let mut sym_index: u16 = 0;
        for len in 1..=16 {
            first_code[len] = code;
            first_symbol[len] = sym_index;
            let cnt = count[len];
            sym_index = sym_index.wrapping_add(cnt);
            code = code.wrapping_add(cnt);
            code <<= 1;
        }

        Ok(Self {
            first_code,
            count,
            first_symbol,
            symbols: values.to_vec(),
        })
    }

    /// Decode one symbol from the bitstream.
    ///
    /// This is a straightforward canonical decode (bit-by-bit); it is slower than
    /// the original decoder's 9-bit fast table, but it is adequate for a reverse-
    /// engineering frame dumper.
    pub fn decode_symbol(&self, br: &mut BitReader<'_>) -> Result<u8> {
        let mut code: u16 = 0;
        for len in 1..=16 {
            code = (code << 1) | (br.read_bits(1)? as u16);
            let first = self.first_code[len];
            let cnt = self.count[len];
            if cnt == 0 {
                continue;
            }
            if code >= first && code < first + cnt {
                let idx = self.first_symbol[len] + (code - first);
                return Ok(self.symbols[idx as usize]);
            }
        }
        bail!("invalid huffman code");
    }
}

